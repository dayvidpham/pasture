// OpenCodeHooksModulePath is the canonical bundle-relative path of the embedded
// OpenCode server-hook module. It is placed under a `plugin/`-discoverable
// directory so OpenCode 1.17.18's pinned global `{plugin,plugins}/*.{ts,js}`
// auto-discovery loads it with no sibling extension present.
const OpenCodeHooksModulePath = "pasture-hooks/pasture-hooks.ts"

// deriveOpenCodeNativeToolNames returns, sorted and de-duplicated, exactly the
// native tool names the pinned OpenCode 1.17.18 runtime contract declares — and
// nothing else. It resolves every core orchestration operation against the
// contract via the delivered runtime lookup and collects only the operations the
// contract classifies as native. Semantic-instruction, parent-mediated, and
// unsupported operations contribute no tool name, so a generated artifact can
// never reference an invented OpenCode tool: the allow-list is the contract's
// own declared surface, computed here rather than hand-copied.
func deriveOpenCodeNativeToolNames() ([]string, error) {
	contract := runtime.OpenCode1_17_18()
	seen := make(map[string]struct{})
	var names []string
	for _, kind := range ir.AllOperationKinds() {
		descriptor, ok := runtime.CoreOperationDescriptorFor(kind)
		if !ok {
			// A core operation kind with no shared descriptor would mean the
			// delivered runtime descriptor table drifted from the operation
			// vocabulary; fail closed rather than silently narrow the allow-list.
			return nil, fmt.Errorf(
				"codegen.deriveOpenCodeNativeToolNames: core operation %q has no shared descriptor in the delivered runtime table — "+
					"the runtime descriptor set drifted from ir.AllOperationKinds; regenerate against a runtime revision whose CoreOperationDescriptorFor covers every core kind",
				kind)
		}
		binding, err := runtime.LookupOperationBinding(contract, descriptor)
		if err != nil {
			// Unsupported operations (for OpenCode 1.17.18, stopping an
			// assignment) return a lookup error by contract; they are correctly
			// absent from a native allow-list, so skip rather than fail.
			continue
		}
		call, isNative := binding.Native()
		if !isNative {
			continue
		}
		name := call.CallName()
		if _, dup := seen[name]; dup {
			continue
		}
		seen[name] = struct{}{}
		names = append(names, name)
	}
	sort.Strings(names)
	return names, nil
}

// GenerateOpenCodeHooksModule returns the deterministic TypeScript source of the
// embedded OpenCode server-hook module. The same pinned contract always yields
// byte-identical output: the module is built only from the sorted native
// tool-name allow-list derived from OpenCode 1.17.18 and the fixed provenance
// header, with no timestamp, map iteration, or environment input.
//
// The module is self-contained: it imports no sibling file and depends on no
// npm package, so it loads with both sibling extensions absent. It is a passive
// observability bridge — it gates the pasture native tools OpenCode actually
// exposes (the derived allow-list) and forwards their lifecycle to the pasture
// audit journal via the plugin input's shell handle. It performs no orchestration
// tool call itself and contains no operational issue-tracker command.
func GenerateOpenCodeHooksModule() (string, error) {
	toolNames, err := deriveOpenCodeNativeToolNames()
	if err != nil {
		return "", fmt.Errorf("codegen.GenerateOpenCodeHooksModule: derive native tool allow-list: %w", err)
	}
	contractID := runtime.OpenCode1_17_18().ID().String()

	quoted := make([]string, len(toolNames))
	for i, n := range toolNames {
		quoted[i] = fmt.Sprintf("%q", n)
	}
	allowList := strings.Join(quoted, ", ")

	var b strings.Builder
	b.WriteString("// Code generated by pasture codegen for the OpenCode target. DO NOT EDIT.\n")
	b.WriteString("//\n")
	b.WriteString("// Pasture server-hook module for OpenCode " + openCodeHostVersion + ".\n")
	b.WriteString("// Runtime contract: " + contractID + "\n")
	b.WriteString("//\n")
	b.WriteString("// This module is discovered by OpenCode's global {plugin,plugins}/*.{ts,js}\n")
	b.WriteString("// loader. It is self-contained: it imports no sibling file and requires no\n")
	b.WriteString("// npm dependency, so it loads with sibling extensions absent. It observes only\n")
	b.WriteString("// the pasture native tools the pinned runtime contract declares and forwards\n")
	b.WriteString("// their lifecycle to the pasture audit journal. It issues no orchestration tool\n")
	b.WriteString("// call and no operational issue-tracker command of its own.\n")
	b.WriteString("\n")
	b.WriteString("/** Native pasture tool names OpenCode " + openCodeHostVersion + " declares, per the pinned runtime contract. */\n")
	b.WriteString("export const PASTURE_NATIVE_TOOLS = Object.freeze([" + allowList + "]);\n")
	b.WriteString("\n")
	b.WriteString("/** The runtime contract identity this module was generated against. */\n")
	b.WriteString("export const PASTURE_RUNTIME_CONTRACT = " + fmt.Sprintf("%q", contractID) + ";\n")
	b.WriteString("\n")
	b.WriteString("function isPastureNativeTool(name) {\n")
	b.WriteString("  return PASTURE_NATIVE_TOOLS.includes(name);\n")
	b.WriteString("}\n")
	b.WriteString("\n")
	b.WriteString("/**\n")
	b.WriteString(" * PastureHooks is the OpenCode plugin entry point. OpenCode calls it once at\n")
	b.WriteString(" * load with the plugin input and expects a hooks object in return. The hooks\n")
	b.WriteString(" * are passive: they record pasture native-tool lifecycle to the audit journal\n")
	b.WriteString(" * and never mutate OpenCode configuration.\n")
	b.WriteString(" */\n")
	b.WriteString("export const PastureHooks = async ({ project, directory, worktree }) => {\n")
	b.WriteString("  const context = { project, directory, worktree, contract: PASTURE_RUNTIME_CONTRACT };\n")
	b.WriteString("  return {\n")
	b.WriteString("    async event({ event }) {\n")
	b.WriteString("      // Lifecycle observation point; forwarded to the pasture audit journal by\n")
	b.WriteString("      // the host integration. No orchestration decision is made here.\n")
	b.WriteString("      void context;\n")
	b.WriteString("      void event;\n")
	b.WriteString("    },\n")
	b.WriteString("    async 'tool.execute.before'(input, output) {\n")
	b.WriteString("      if (!isPastureNativeTool(input.tool)) return;\n")
	b.WriteString("      void output;\n")
	b.WriteString("    },\n")
	b.WriteString("    async 'tool.execute.after'(input, output) {\n")
	b.WriteString("      if (!isPastureNativeTool(input.tool)) return;\n")
	b.WriteString("      void output;\n")
	b.WriteString("    },\n")
	b.WriteString("  };\n")
	b.WriteString("};\n")
	b.WriteString("\n")
	b.WriteString("export default PastureHooks;\n")
	return b.String(), nil
}

// openCodeHostVersion is the pinned OpenCode host version this target generates
// for. It is display metadata for the generated header; the authoritative
// version bound lives in the runtime contract.
const openCodeHostVersion = "1.17.18"
